	/**
	 * one_icon_for_wp_icons constructor.
	 */
	function __construct() {
		// Add own icons to icon list
		add_filter( 'one_icon_for_wp_icon_list', array( __CLASS__, 'add_font_awesome' ) );
	}

	/**
	 * Extent icon list.
	 *
	 * @param array $icon_list
	 *
	 * @return array
	 */
	public static function add_font_awesome( array $icon_list ): array {
		return array_merge( $icon_list, self::get_font_awesome_icon_list() );
	}

	/**
	 * Returns a list of font awesome icons as key value array.
	 *
	 * @return array
	 */
	private static function get_font_awesome_icon_list(): array {

		// Prepare empty array
		$icon_list = array();

		// List of styles
		$icon_styles = array(
			'solid',
			'brands',
		);

		// Loop styles
		foreach ( $icon_styles as $style ) {

			// Build getter method name
			$icon_getter = 'get_' . $style . '_icons';

			// Loop icons
			foreach ( self::$icon_getter() as $name => $label ) {
				// Add icon to array
				$icon_list[] = array(
					'value' => 'font-awesome/' . $style . '/' . $name,
					'label' => $label,
				);
			}
		}

		return $icon_list;
	}

